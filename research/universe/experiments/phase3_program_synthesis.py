# ============================================================
# Pattern Detection (simulates what LLM would do)
# ============================================================

def detect_log_pattern(text):
    """Detect log-like patterns and extract template + variables.

    Example:
      Input: "[INFO] User 42 logged in at 10:23:45\n[INFO] User 17 logged in at 10:24:01\n..."
      Output:
        template: "[INFO] User {} logged in at {}"
        variables: [("42", "10:23:45"), ("17", "10:24:01"), ...]
    """
    lines = text.strip().split('\n')

    # Find common pattern by replacing numbers/IDs with {}
    templates = []
    variables = []

    for line in lines:
        # Replace numbers, IPs, timestamps with {}
        pattern = re.sub(r'\d+', '{}', line)
        pattern = re.sub(r'\d+\.\d+\.\d+\.\d+', '{}', pattern)
        templates.append(pattern)

        # Extract the actual values
        values = re.findall(r'\d+\.?\d*', line)
        variables.append(values)

    # Find most common template
    template_counts = Counter(templates)
    most_common_template, count = template_counts.most_common(1)[0]

    # Check if we have multiple templates
    if len(template_counts) > 1:
        # Mixed templates — less compression
        return None, None, 0.0

    # Reconstruct to verify
    return most_common_template, variables, count / len(lines)


def detect_json_pattern(text):
    """Detect JSON array patterns."""
    try:
        data = json.loads(text)
        if isinstance(data, list) and len(data) > 1:
            # Extract keys from first object
            if isinstance(data[0], dict):
                keys = list(data[0].keys())
                template = json.dumps({k: None for k in keys})
                # Extract values
                values = []
                for item in data:
                    row = [item.get(k) for k in keys]
                    values.append(row)
                return template, values, 1.0
    except (json.JSONDecodeError, TypeError):
        pass
    return None, None, 0.0


def detect_csv_pattern(text):
    """Detect CSV patterns."""
    lines = text.strip().split('\n')
    if len(lines) < 3:
        return None, None, 0.0

    # Check if comma-separated with consistent columns
    header = lines[0].split(',')
    if len(header) < 2:
        return None, None, 0.0

    consistent = all(len(line.split(',')) == len(header) for line in lines[1:])
    if not consistent:
        return None, None, 0.0

    template = ','.join(header)
    values = [line.split(',')[1:] for line in lines[1:]]  # Skip header in values
    return template, values, 1.0


# ============================================================
# Program Synthesis Compressor
# ============================================================

def compress_with_program(text):
    """Compress text by extracting pattern + variables.

    Returns (compressed_size, method_used, details)
    """
    original_size = len(text.encode('utf-8'))
    zip_size = len(zlib.compress(text.encode('utf-8'), 9))

    # Try each pattern detector
    detectors = [
        ('json', detect_json_pattern),
        ('csv', detect_csv_pattern),
        ('log', detect_log_pattern),
    ]

    best_result = None

    for name, detector in detectors:
        template, variables, coverage = detector(text)
        if template is None or coverage < 0.5:
            continue

        # Compress template + variables separately
        template_bytes = template.encode('utf-8')
        template_compressed = zlib.compress(template_bytes, 9)

        # Serialize variables
        if isinstance(variables, list) and variables and isinstance(variables[0], list):
            # 2D list (CSV/JSON values)
            var_str = '\n'.join(','.join(str(v) for v in row) for row in variables)
        else:
            # List of lists (log values)
            var_str = '\n'.join(','.join(str(v) for v in vals) for vals in variables)

        var_bytes = var_str.encode('utf-8')
        var_compressed = zlib.compress(var_bytes, 9)

        total = len(template_compressed) + len(var_compressed) + 8  # +8 for sizes

        if best_result is None or total < best_result['total']:
            best_result = {
                'method': name,
                'template_size': len(template_compressed),
                'variables_size': len(var_compressed),
                'total': total,
                'coverage': coverage,
            }

    if best_result:
        return best_result['total'], best_result['method'], best_result
    else:
        return zip_size, 'zlib_fallback', {'total': zip_size, 'coverage': 0.0}


