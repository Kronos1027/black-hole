def generate_log_files(n_files=20, n_lines=100, seed=42):
    """Generate synthetic log files."""
    rng = np.random.default_rng(seed)
    files = []
    for i in range(n_files):
        lines = []
        for j in range(n_lines):
            user = rng.integers(1, 1000)
            hour = rng.integers(0, 24)
            minute = rng.integers(0, 60)
            second = rng.integers(0, 60)
            line = f"[INFO] User {user} logged in at {hour:02d}:{minute:02d}:{second:02d}"
            lines.append(line)
        files.append('\n'.join(lines))
    return files


def generate_json_files(n_files=20, n_items=100, seed=42):
    """Generate synthetic JSON array files."""
    rng = np.random.default_rng(seed)
    files = []
    for i in range(n_files):
        data = []
        for j in range(n_items):
            item = {
                "id": int(rng.integers(1, 10000)),
                "name": f"item_{rng.integers(1, 100)}",
                "value": int(rng.integers(0, 1000)),
                "active": bool(rng.integers(0, 2)),
            }
            data.append(item)
        files.append(json.dumps(data))
    return files


def generate_csv_files(n_files=20, n_rows=100, seed=42):
    """Generate synthetic CSV files."""
    rng = np.random.default_rng(seed)
    files = []
    for i in range(n_files):
        lines = ["id,name,value,score"]
        for j in range(n_rows):
            line = f"{rng.integers(1, 10000)},item_{rng.integers(1, 100)},{rng.integers(0, 1000)},{rng.integers(0, 100)}"
            lines.append(line)
        files.append('\n'.join(lines))
    return files


